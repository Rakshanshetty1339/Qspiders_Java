class Relational_Operator{
public static void main(String[]args){
char a='A',b='B';
System.out.println(a==b);
System.out.println(a>b);
System.out.println(a<b);
System.out.println('A'=='B');
System.out.println('A'>='B');
System.out.println('A'<='B');
System.out.println(a!=b);
System.out.println('A'!='B');
}
}